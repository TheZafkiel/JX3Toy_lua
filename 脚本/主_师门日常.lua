function 师门初始化设置并执行()
	local szKeyName = '日常师门'
	设置_复活类型('原地复活')
	设置_自动换新装备('是')
	设置_自动换新背包('是')
	设置_自动ROLL('是')
	设置_各职业技能方案()
	队伍_禁止组队()
	队伍_设置主动组队('否')
	local 师表 = 取当前职业师门任务名()
	if not 师表 then
		OutputMessage('该职业没有设置师门任务')
		return
	end
	if AutoFunction.GetRoleszMapName() ~= 师表.szMapName then
		交通_寻路神行车夫(师表.szMapName,师表.x,师表.y,师表.z,'卡点下线')
	end
	执行_副脚本按每天重置执行(师表.副脚本名)
	自定义账号状态(string.format("%s日常师门",时间_取时间段日期()))
	自定义账号状态('勾选',0)
	延时(8000)
	结束游戏()
end

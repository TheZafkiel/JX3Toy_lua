function DPS()
    if nobuff('弹跳') and nocd('扶摇直上') then
        cast('扶摇直上')
    end
    -- 战前准备
    ZQZB()
    -- 普通技能释放 -- /cast [rage>89] 啸日
    if mount('问水诀') then
        QJ()
        if rage() > 89 then
            cast('啸日')
        end
    end
    if mount('山居剑意') then
        ZJ()
        if rage() < 15 then
            cast('啸日')
        end
    end
end

-- 是否暂停
function isRun(arg1, arg2, arg3)
    return time() >= ZD_ZT_TIME
end
-- 暂停 T 毫秒
function suspension(t)
    ZD_ZT_TIME = t + time()
end

-- 一段跳
function jump_1(params)
    if FYZT == 0 then
        -- statements
        FYZT = 1
        FYZT_ARR_time[1] = time()
        jump()
    end
    if FYZT == 2 then
        -- statements
        FYZT_ARR_time[2] = time()
        jumpf()
        FYZT = 3
    end
    if FYZT == 4 then
        FYZT_ARR_time[3] = time()
        cast('蹑云逐月')
        cast('凌霄揽胜')
        cast('瑶台枕鹤')
        cast('迎风回浪')
        FYZT = 5
    end
    if FYZT == 6 then
        -- statements
        jump()
        FYZT = 7
    end
end


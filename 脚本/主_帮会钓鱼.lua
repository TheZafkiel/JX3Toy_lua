function 帮会钓鱼初始化()
	设置_复活类型('原地复活')
	设置_自动换新装备('是')
	设置_自动换新背包('是')
	设置_自动ROLL('是')
	设置_各职业技能方案()
	队伍_禁止组队()
	队伍_设置主动组队('否')
	设置_自动复活('是')
	if AutoFunction.GetRoleszMapName() ~= '帮会领地' then
		执行_副脚本("帮会钓鱼.lua")
	else
		if 执行_继续上次脚本记录行数() == 0 then
			执行_副脚本("帮会钓鱼.lua")
		end
	end
end

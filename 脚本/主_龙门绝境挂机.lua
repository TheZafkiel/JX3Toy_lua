function 龙门绝境挂机初始化()
	设置_复活类型('原地复活')
	设置_自动换新装备('是')
	设置_自动换新背包('是')
	设置_自动ROLL('是')
	设置_各职业技能方案()
	队伍_禁止组队()
	队伍_设置主动组队('否')
	技能_切换心法('攻击')
	if AutoFunction.GetRoleszMapName() ~= '龙门绝境' then
		执行_副脚本("龙门绝境挂机.lua")
	else
		if 执行_继续上次脚本记录行数() == 0 then
			执行_副脚本("龙门绝境挂机.lua")
		end
	end
end

	队伍_设置主动组队('否')
	local 阵营名 = GetClientPlayerTB().szCampName
	if 阵营名 == '恶人谷' then
		if 取今天星期几() == '星期天' then
--[[			if 时间_日期执行判断首次执行("攻防设置") then

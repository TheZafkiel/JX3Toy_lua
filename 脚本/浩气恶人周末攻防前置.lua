				if 执行_继续上次脚本记录行数() == 0 then
					执行_副脚本('浩气_南屏攻防前置任务.lua')
				end
			end
			时间_日期执行完成("攻防设置")--]]
			执行_副脚本按每天重置执行("浩气_南屏攻防前置任务.lua")
		end
		if 取今天星期几() == '星期天' then
			执行_副脚本按每天重置执行("浩气_昆仑攻防前置任务.lua")
		end
	end
end
